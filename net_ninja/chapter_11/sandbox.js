//Switch statements
 let grade = 'A';

 switch(grade){
 	case 'A':
 		console.log("You got an A");
 		break ;
 	case 'B':
 		console.log("You got a B");
 		break ;
 	case 'C':
 		console.log("You got a C");
 		break ;
 	case 'D':
 		console.log("You got a D");
 		break ;
 	default:
 		console.log("Invalid grade");
 }
 //Note switch statements use strict comparison